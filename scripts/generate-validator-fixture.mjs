/**
 * Generates a comprehensive .odt file exercising all major odf-kit features.
 *
 * Run after `npm run build` from the repo root:
 *   node scripts/generate-validator-fixture.mjs
 *
 * Output: validator-fixture.odt (in the current working directory)
 *
 * Used by CI to feed a real generated document to the OASIS ODF Validator.
 * Covers: page layout, headers/footers, headings (all 6 levels), paragraphs,
 * all character formatting, paragraph options, bullet/numbered/alpha/roman
 * lists, tables with cell formatting and spans, hyperlinks.
 */

import { OdtDocument } from "../dist/index.js";
import { writeFileSync } from "fs";

const doc = new OdtDocument();

// Metadata
doc.setMetadata({
  title: "odf-kit Validator Fixture",
  creator: "odf-kit CI",
  description: "Comprehensive fixture for ODF spec compliance validation",
});

// Page layout
doc.setPageLayout({
  width: "21cm",
  height: "29.7cm",
  marginTop: "2cm",
  marginBottom: "2cm",
  marginLeft: "2.5cm",
  marginRight: "2.5cm",
});

// Header and footer
doc.setHeader((h) => {
  h.addText("odf-kit Validator Fixture", { bold: true });
  h.addText(" — Page ");
  h.addPageNumber();
});
doc.setFooter("Generated by odf-kit CI — ODF Spec Compliance Fixture");

// ── Headings (all 6 levels) ────────────────────────────────────────────────

doc.addHeading("Level 1 Heading", 1);
doc.addHeading("Level 2 Heading", 2);
doc.addHeading("Level 3 Heading", 3);
doc.addHeading("Level 4 Heading", 4);
doc.addHeading("Level 5 Heading", 5);
doc.addHeading("Level 6 Heading", 6);

// Heading with paragraph options
doc.addHeading("Centered Heading", 1, { align: "center" });

// ── Character formatting ───────────────────────────────────────────────────

doc.addParagraph("Plain paragraph with no formatting.");

doc.addParagraph((p) => {
  p.addText("Bold", { bold: true });
  p.addText(" | ");
  p.addText("Italic", { italic: true });
  p.addText(" | ");
  p.addText("Underline", { underline: true });
  p.addText(" | ");
  p.addText("Strikethrough", { strikethrough: true });
  p.addText(" | ");
  p.addText("Super", { superscript: true });
  p.addText("script | ");
  p.addText("Sub", { subscript: true });
  p.addText("script");
});

doc.addParagraph((p) => {
  p.addText("Bold+Italic", { bold: true, italic: true });
  p.addText(" | ");
  p.addText("Arial 14pt", { fontFamily: "Arial", fontSize: 14 });
  p.addText(" | ");
  p.addText("Red text", { color: "#CC0000" });
  p.addText(" | ");
  p.addText("Highlighted", { highlightColor: "#FFFF00" });
});

doc.addParagraph((p) => {
  p.addText("UPPERCASE", { textTransform: "uppercase" });
  p.addText(" | ");
  p.addText("Small Caps", { smallCaps: true });
  p.addText(" | ");
  p.addText("Weight 300", { fontWeight: 300 });
  p.addText(" | ");
  p.addText("Weight 700", { fontWeight: 700 });
});

// ── Paragraph options ──────────────────────────────────────────────────────

doc.addParagraph("Left aligned paragraph.", { align: "left" });
doc.addParagraph("Center aligned paragraph.", { align: "center" });
doc.addParagraph("Right aligned paragraph.", { align: "right" });
doc.addParagraph(
  "Justified paragraph with enough text to actually demonstrate justification across the full width.",
  { align: "justify" }
);
doc.addParagraph("Paragraph with space before and after.", {
  spaceBefore: "0.5cm",
  spaceAfter: "0.5cm",
});
doc.addParagraph("Paragraph with 1.5x line height.", { lineHeight: 1.5 });
doc.addParagraph("Paragraph with left indent and first-line indent.", {
  indentLeft: "1cm",
  indentFirst: "0.5cm",
});

// ── Lists ──────────────────────────────────────────────────────────────────

doc.addList(["Bullet item one", "Bullet item two", "Bullet item three"]);

doc.addList(["Numbered item one", "Numbered item two", "Numbered item three"], {
  type: "numbered",
});

doc.addList(["Alpha item a", "Alpha item b", "Alpha item c"], {
  type: "numbered",
  numFormat: "a",
});

doc.addList(["Roman item i", "Roman item ii", "Roman item iii"], {
  type: "numbered",
  numFormat: "i",
});

doc.addList(["Roman caps I", "Roman caps II"], {
  type: "numbered",
  numFormat: "I",
});

doc.addList(["Start at 5", "Then 6", "Then 7"], {
  type: "numbered",
  startValue: 5,
});

// ── Tables ─────────────────────────────────────────────────────────────────

// Simple table
doc.addTable([
  ["Name", "Role", "Location"],
  ["Alice", "Engineer", "Remote"],
  ["Bob", "Designer", "Office"],
]);

// Table with cell and row formatting
doc.addTable((t) => {
  t.addRow(
    (r) => {
      r.addCell("Header spanning two columns", {
        colSpan: 2,
        bold: true,
        backgroundColor: "#CCCCCC",
        verticalAlign: "middle",
        padding: "0.15cm",
      });
    },
    { backgroundColor: "#CCCCCC" }
  );
  t.addRow((r) => {
    r.addCell("Cell with border", {
      border: "0.5pt solid #000000",
    });
    r.addCell("Cell with top/bottom borders", {
      borderTop: "1pt solid #333333",
      borderBottom: "1pt solid #333333",
    });
  });
  t.addRow((r) => {
    r.addCell("Tall cell", { rowSpan: 2, verticalAlign: "top" });
    r.addCell("Normal cell");
  });
  t.addRow((r) => {
    r.addCell("Adjacent to rowspan");
  });
});

// ── Hyperlinks ─────────────────────────────────────────────────────────────

doc.addParagraph((p) => {
  p.addText("Visit ");
  p.addLink("the odf-kit repository", "https://github.com/GitHubNewbie0/odf-kit");
  p.addText(" for more information.");
});

doc.addParagraph((p) => {
  p.addLink("Bold link", "https://example.com", { bold: true });
});

// ── Page break + second page ───────────────────────────────────────────────

doc.addPageBreak();
doc.addHeading("Second Page Content", 1);
doc.addParagraph("This content appears on the second page after an explicit page break.");

// ── Save ───────────────────────────────────────────────────────────────────

const bytes = await doc.save();
writeFileSync("validator-fixture.odt", bytes);
console.log("validator-fixture.odt written successfully.");
